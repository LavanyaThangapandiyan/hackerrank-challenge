import java.util.*;
import java.util.Scanner;

public class StdinStdout {

    public static void main(String[] args)
     {
        Scanner s=new Scanner(System.in);
        int a=s.nextInt();
        int b=s.nextInt();
        int c=s.nextInt();
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
         
    }
}
